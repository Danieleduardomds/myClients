public class Clientes {

    private int Id;
    private String Nome;
    private String Telefone;
    private String Celular;
    private String Email;
    private String Senha;

    public Clientes(int id, String nome, String telefone, String celular, String email, String senha) {
        this.Id = id;
        this.Nome = nome;
        this.Telefone = telefone;
        this.Celular = celular;
        this.Email = email;
        this.Senha = senha;
    }   
    

}
